<script>
import AppHeader from '../components/AppHeader.vue';

export default {
  components: {
    AppHeader
  },
  data() {
    return {
      cardNumber: '',
      cardExpiry: '',
      cardName: '',
      amount: null
    };
  },
  methods: {
    async addBalance() {
      const user = JSON.parse(localStorage.getItem('user'));
      if (!user) {
        alert('Debes iniciar sesión para añadir saldo.');
        return;
      }

      try {
        const response = await fetch('http://localhost:8080/index.php?path=add-balance', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json'
          },
          body: JSON.stringify({
            userId: user.id,
            amount: this.amount,
            cardNumber: this.cardNumber,
            cardExpiry: this.cardExpiry,
            cardName: this.cardName
          })
        });

        const data = await response.json();

        if (data.status === 'Balance added') {
          alert('Saldo añadido con éxito.');
        } else {
          alert('Error al añadir saldo.');
        }
      } catch (error) {
        console.error('Error añadiendo saldo:', error);
        alert('Error añadiendo saldo. Por favor, inténtalo de nuevo.');
      }
    }
  }
};
</script>

